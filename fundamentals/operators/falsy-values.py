# Falsy values
## Prints out the next value that is not falsy

## OR operators

print(0 or 1) ## returns 1
print(False or 'hey') ## returns 'hey'
print('hi' or 'hey') ## returns 'hi'
print([] or False) ## returns 'False'
print(False or []) ## returns '[]'

## AND operators

print(0 and 1) ## returns 0
print(1 and 0) ## returns 0
print(False and 'hey') ## returns False
print('hi' and 'hey') ## returns 'hey'
print([] and False) ## returns '[]'
print(False and []) ## returns 'False'
